const x = 51, y = 5;

// +
console.log(`${x} + ${y} = ${x + y}`);

// -
console.log(`${x} - ${y} = ${x - y}`);

// *
console.log(`${x} * ${y} = ${x * y}`);

// /
console.log(`${x} / ${y} = ${x / y}`);

// %
console.log(`${x} % ${y} = ${x % y}`);

// **
console.log(`${x} ** ${y} = ${x ** y}`);

// string + string -> string
console.log('this ' + 'is ' + 'my ' + 'text');

// +value -> number
const valueToNumber = +'123';
console.log('valueToNumber');

// !!value -> boolean
const valueTooBoolean = !!'some text';
console.log(valueTooBoolean);

// ++
let a = 10;
console.log(a++);
console.log(++a);

// --
a = 10;
console.log(a--);
console.log(--a);
